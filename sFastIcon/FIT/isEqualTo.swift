//Write a function called “isEqualTo”.
//Given 2 numbers, “isEqualTo” returns whether num2 is equal to num1.

import Foundation

func isEqualTo(num1: Int, num2: Int) -> Bool {
  var areEqual: Bool = false
  if (num1 == num2) {
    areEqual = true
  }
  return areEqual
}
